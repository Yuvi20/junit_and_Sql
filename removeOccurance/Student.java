package removeOccurance;

public class Student {
	 // Data members
	 private String name;
	 private int score;
	 
	 // Constructor with public visibility
	 public Student(String name, int score) {
	     this.name = name;
	     this.score = score;
	 }
	 
	 // Getter methods for name and score
	 public String getName() {
	     return name;
	 }
	 
	 public int getScore() {
	     return score;
	 }
	}
